"""Serialise crawl scenarios into an executable pytest-playwright script (Skript_C)."""

from __future__ import annotations

import time

from config.settings import Settings, TargetApp, get_settings
from tcgen.orchestration.models import GeneratedScript, Pipeline
from tcgen.pipelines.crawler.crawler import Crawler, CrawlOutput

_HEADER = '''"""Skript_C — autonomously generated Web-UI test suite (traditional crawler).

Target : {name} ({base_url})
States : {n_states}   Transitions: {n_edges}   Crawl time: {elapsed}s
Generated by the crawler pipeline; no user story was consulted.
"""

import pytest
from playwright.sync_api import Page, expect
'''

_TEST_TEMPLATE = '''

def test_scenario_{idx}(page: Page) -> None:
    """{summary}"""
{body}
    # Robust smoke assertion: body present in the DOM (avoids false "hidden"
    # failures when a modal/sidenav overlay is open at the end of the scenario).
    expect(page.locator("body")).to_be_attached()
'''


def _render_scenario(idx: int, actions) -> str:
    lines: list[str] = []
    for action in actions:
        for code in action.code_lines("page"):
            lines.append(f"    {code}")
    # NOTE: per-step not_to_have_url("about:blank") was removed — it fires as a
    # Playwright action and triggers locator handlers (e.g. cookie banner), causing
    # page.url to return None and false failures on SPAs. The final body assertion
    # below is sufficient as a sanity check.
    summary = " -> ".join(a.describe() for a in actions)[:200]
    body = "\n".join(lines)
    return _TEST_TEMPLATE.format(idx=idx, summary=summary, body=body)


def serialize(crawl: CrawlOutput, target: TargetApp) -> str:
    """Render a full pytest module from crawl scenarios."""
    header = _HEADER.format(
        name=target.name,
        base_url=target.base_url,
        n_states=crawl.n_states,
        n_edges=crawl.n_edges,
        elapsed=crawl.elapsed_s,
    )
    parts = [header]
    for i, scenario in enumerate(crawl.scenarios, start=1):
        parts.append(_render_scenario(i, scenario))
    return "".join(parts)


def _element_coverage(crawl: CrawlOutput) -> int:
    """Distinct DOM elements interacted with across the whole suite."""
    sigs = set()
    for scenario in crawl.scenarios:
        for action in scenario:
            if action.element is not None:
                sigs.add(action.element.signature())
    return len(sigs)


def generate(target: TargetApp, settings: Settings | None = None,
             phase=None) -> GeneratedScript:
    """Run the crawler and return the serialised ``Skript_C`` as a model."""
    settings = settings or get_settings()
    t0 = time.time()
    if getattr(settings, "crawler_backend", "bfs") == "crawlee":
        from tcgen.pipelines.crawler.crawlee_crawler import CrawleeCrawler
        crawl = CrawleeCrawler(target, settings).crawl(phase=phase)
    else:
        crawl = Crawler(target, settings).crawl(phase=phase)
    code = serialize(crawl, target)
    return GeneratedScript(
        pipeline=Pipeline.CRAWLER,
        app_key=target.key,
        language="python",
        code=code,
        generation_time_s=round(time.time() - t0, 2),
        model="(none)",
        provider="crawler",
        meta={
            "n_states": crawl.n_states,
            "n_edges": crawl.n_edges,
            "n_scenarios": len(crawl.scenarios),
            "element_coverage": _element_coverage(crawl),
            "discovered_elements": crawl.discovered_elements,
            "crawl_time_s": crawl.elapsed_s,
            # Grounding for the hybrid (Skript_H): real verified locators + routes.
            "locator_catalog": crawl.locator_catalog,
            "routes": crawl.routes,
        },
    )
