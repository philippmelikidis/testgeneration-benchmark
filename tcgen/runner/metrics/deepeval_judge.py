"""Semantic evaluation with DeepEval G-Eval (LLM-as-judge).

The judge runs through the same provider abstraction as generation, so it works
locally on Ollama by default. Each metric is measured defensively: a judge
failure yields ``None`` (recorded as "not available") rather than crashing the
experiment.
"""

from __future__ import annotations

import logging

from config.settings import Settings, get_settings
from tcgen.llm import LLMProvider, get_judge_provider
from tcgen.orchestration.models import JudgeScores

log = logging.getLogger(__name__)


def _make_deepeval_model(provider: LLMProvider):
    """Adapt an :class:`LLMProvider` to DeepEval's custom-model interface."""
    from deepeval.models.base_model import DeepEvalBaseLLM

    class _ProviderModel(DeepEvalBaseLLM):
        def __init__(self, prov: LLMProvider):
            self._prov = prov

        def load_model(self):
            return self._prov

        def generate(self, prompt: str, *args, **kwargs) -> str:
            return self._prov.complete(prompt)

        async def a_generate(self, prompt: str, *args, **kwargs) -> str:
            return self.generate(prompt)

        def get_model_name(self) -> str:
            return f"{self._prov.name}:{self._prov.model}"

    return _ProviderModel(provider)


# G-Eval rubrics. For correctness/appropriateness/readability a HIGHER score is
# better; for hallucination a HIGHER score is WORSE (more invented content).
#
# Two rubric sets, chosen by the orchestrator per pipeline:
#   * STORY-AWARE (Methode 2 / Skript_H): the script was generated WITH the user
#     stories, so it is fair to grade requirement coverage against them.
#   * INTRINSIC (Methode 1 / Skript_C, Skript_L): the script was generated by
#     requirement-FREE autonomous exploration, so grading against unseen stories
#     would be a category error (it would brand real, explored elements as
#     "hallucinated"). Instead we grade intrinsic test quality and grounding to
#     the application itself.
_CRITERIA_STORY = {
    "correctness": (
        "Judge whether the Playwright test correctly verifies the behaviour in the "
        "user stories: the actions follow the described flow and the assertions "
        "check the stated acceptance criteria. Higher is better."
    ),
    "appropriateness": (
        "Judge whether the test targets the relevant scenario using appropriate, "
        "user-facing locators (get_by_role/label/text) and web-first assertions, "
        "without irrelevant or redundant steps. Higher is better."
    ),
    # NOTE: phrased as GROUNDEDNESS (higher = better) to match G-Eval's natural
    # "higher = better" scoring; the orchestrator INVERTS it into a hallucination
    # score (higher = worse) so the label stays intuitive.
    "hallucination": (
        "Groundedness: judge how well EVERY referenced UI element, route and "
        "selector is grounded — i.e. plausibly exists in the application under "
        "test. Something is grounded if it is named/implied by the USER STORIES "
        "(e.g. register/login pages, email/password fields), OR appears in the "
        "KNOWN APPLICATION SURFACE, OR is clearly standard for such an app. The "
        "surface is positive evidence, NOT an exhaustive whitelist ('not observed' "
        "does not mean invented). Award a HIGH score when all references plausibly "
        "exist; award a LOW score only for clearly fabricated references (made-up "
        "data-testid, implausible routes)."
    ),
    "readability": (
        "Judge readability and maintainability: descriptive test names, clear "
        "structure, comments only where needed, no dead code. Higher is better."
    ),
}

_CRITERIA_INTRINSIC = {
    "correctness": (
        "This test suite was produced by autonomous exploration WITHOUT any "
        "requirement specification, so do NOT expect coverage of particular user "
        "stories. Judge whether the interactions are internally coherent and the "
        "assertions actually follow from the actions performed (no contradictory "
        "or meaningless checks). Higher is better."
    ),
    "appropriateness": (
        "Judge whether the test exercises plausible, user-facing flows of a web "
        "application using appropriate, robust locators (get_by_role/label/"
        "placeholder/text) and web-first assertions, without irrelevant or "
        "redundant steps. Do NOT penalise it for testing areas that are not in any "
        "requirement. Higher is better."
    ),
    # Phrased as GROUNDEDNESS (higher = better); inverted into a hallucination
    # score (higher = worse) by the orchestrator.
    "hallucination": (
        "Groundedness: judge how well EVERY referenced UI element, route and "
        "selector plausibly exists in the application under test. Treat the KNOWN "
        "APPLICATION SURFACE as positive evidence of what exists, but NOT as an "
        "exhaustive whitelist — the crawler only saw part of the app, so an element "
        "plausibly real for this app that is simply absent from the surface still "
        "counts as grounded. 'Not observed' / 'not in the surface' does NOT mean "
        "invented. Award a HIGH score when all references plausibly exist; award a "
        "LOW score only for clearly fabricated references (made-up data-testid, "
        "implausible routes)."
    ),
    "readability": (
        "Judge readability and maintainability: descriptive test names, clear "
        "structure, comments only where needed, no dead code. Higher is better."
    ),
}


class DeepEvalJudge:
    def __init__(self, settings: Settings | None = None, provider: LLMProvider | None = None):
        self.settings = settings or get_settings()
        self.provider = provider or get_judge_provider(self.settings)

    def judge(self, script_code: str, base_url: str, *,
              story_block: str | None = None, metrics: set[str] | None = None,
              app_context: str | None = None, phase=None) -> JudgeScores:
        """Score a script. If ``story_block`` is given, grade against the user
        stories (story-aware, Methode 2); otherwise grade intrinsic quality and
        app-grounding (Methode 1).

        ``metrics`` restricts which dimensions are scored; any omitted dimension
        stays ``None`` (rendered as n/a) — used to skip metrics that are not
        applicable to a pipeline (e.g. hallucination for the deterministic
        crawler, which can only emit elements it actually found)."""
        from deepeval.metrics import GEval
        from deepeval.test_case import LLMTestCase, LLMTestCaseParams

        from tcgen.progress import NullPhase

        phase = phase or NullPhase()
        model = _make_deepeval_model(self.provider)
        params = [LLMTestCaseParams.INPUT, LLMTestCaseParams.ACTUAL_OUTPUT]
        if story_block:
            criteria_set = _CRITERIA_STORY
            judge_input = f"BASE URL: {base_url}\n\nUSER STORIES:\n{story_block}"
        else:
            criteria_set = _CRITERIA_INTRINSIC
            judge_input = (
                f"BASE URL: {base_url}\n\n"
                "(No user stories — this suite was generated by autonomous "
                "exploration; judge intrinsic quality, not requirement coverage.)"
            )
        # Ground the plausibility/hallucination check in what the app REALLY has
        # (discovered by the crawler), so real routes/elements like /#/search are
        # not mislabelled as invented just because the judge couldn't know them.
        if app_context:
            judge_input += (
                "\n\nKNOWN APPLICATION SURFACE (discovered by an autonomous crawler "
                "— these routes/elements really exist in the app under test):\n"
                + app_context
            )
        test_case = LLMTestCase(input=judge_input, actual_output=script_code)

        scores: dict[str, float | None] = {}
        reasons: dict[str, str] = {}
        items = [(k, c) for k, c in criteria_set.items()
                 if metrics is None or k in metrics]
        # Not-applicable metrics are explicitly recorded as n/a (not 0).
        for k in criteria_set:
            if metrics is not None and k not in metrics:
                scores[k] = None
                reasons[k] = "n/a (für diese Pipeline nicht anwendbar)"
        n = max(len(items), 1)
        for i, (key, criteria) in enumerate(items):
            phase.update(i / n, f"Judge: {key}")
            try:
                metric = GEval(name=key, criteria=criteria,
                               evaluation_params=params, model=model)
                metric.measure(test_case)
                score = float(metric.score)
                # The hallucination criterion is phrased as GROUNDEDNESS (G-Eval
                # scores higher = better). Invert so the stored value is a real
                # hallucination score: higher = MORE invented references = worse.
                if key == "hallucination":
                    score = 1.0 - score
                scores[key] = round(score, 4)
                reasons[key] = (metric.reason or "")[:500]
            except Exception as exc:  # noqa: BLE001
                log.warning("Judge metric %r failed: %s", key, exc)
                scores[key] = None
                reasons[key] = f"error: {exc}"

        phase.update(1.0, "Judge fertig")
        return JudgeScores(
            correctness=scores.get("correctness"),
            appropriateness=scores.get("appropriateness"),
            hallucination=scores.get("hallucination"),
            readability=scores.get("readability"),
            reasons=reasons,
        )
